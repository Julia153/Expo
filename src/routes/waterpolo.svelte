<h1 class="title">Waterpolo</h1>
<h2 class="subtitle" id="when">When: Games Tuesday or Wednesday latest game at 9pm, training times vary</h2>
<h2 class="subtitle" id="where">Where: Training - School pool  Games - Jellie Park/ QEII/ Rangi</h2>
<p>Waterpolo is a competitive team sport played in the water between two teams. The game contains 4 quarters in which each team will try to score a goal by throwing the ball into the goal at each end. </p>
<br>
<p>You will need your own Rangi waterpolo togs and swimming cap, waterpolo caps and balls are provided. The cost is about $90 per term plus pool entry and transport. The Rangi togs available from the uniform shop are about $110. No experiance needed but a good swimming ability is a big advantage. Teams may enter south island tournaments.</p>
