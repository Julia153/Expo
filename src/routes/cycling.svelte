<h1 class="title">Cycling</h1>
<h2 class="subtitle" id="when">When: Sunday rides in term 1, Wednesday afternoons in term 2 & 3 and Friday wind-trainer sessions in terms 1-3. </h2>
<h2 class="subtitle" id="where">Where: Rides leave from Rangi, terms 2 & 3 cycle in Tai Tapu</h2>
<p> Cycling is split into 4 races for the school competition- individual time trials which is you racing as fast as you can against the clock, team time trials which is a group of min 3 and max 4 people (max 5 for nationals) and you can start the race with 4 or 5 and finish with a min of 3 and a graded scratch where you race in a group of people about your level and it is the first person over the finish line who wins. For training you train as a team to learn how to do team time trials.</p>
<br>
<p>You will need your own road bike and helmet, you will need a vest that you MUST wear to and from races.The cost is $35 per term, there are additional costs for attending events and purchasing gear. No experiance is needed, all levels will be catered for.</p>
