<p><b>Years 7-10</b> rehearse together once a week to learn about choir singing through a range of songs. Rangi Voices gives performances at school events (e.g. Clan and Carol services), sings in the community - e.g with choirs from other Independant Schools - and takes part in the annual national Big Sing competition.</p>
