<h2 class="subtitle" id="where">Where: Music Block</h2>
<p>You will learn about playing jazz and improvising.  Prepare for lots of gigs - at school, in the community - Big Band Festival, Southern Jam, play at cafes/bars/clubs, prepare for competitions.</p>
