<h1 class="title">Cross Country Running</h1>
<h2 class="subtitle" id="when">When: Saturdays terms 2 & 3 </h2>
<h2 class="subtitle" id="where">Where: Rangi and various other venues</h2>
<p>Cross country running is a sport in which teams and individuals run a race on open-air courses over natural terrain such as dirt or grass.</p>
<br>
<p>You will need your own running shoes which you can buy at most sport shop and running uniform which you can purchase at the uniform shop. The cost depends on which events you choose to enter in and the transport you take. You can also consider training with a club outside of school, the team may be selected to go to nationals.</p>
